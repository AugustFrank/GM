using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using GM011.Models;
using GM11.Models;

namespace GM11.Controllers
{
    public class OrdersController : Controller
    {
        private readonly GMContext _context;

        public OrdersController(GMContext context)
        {
            _context = context;    
        }

        // GET: Orders måske loade flere ind f.eks. costumer
        public async Task<IActionResult> Index()
        {
            var gMContext = await _context.Order
                .Include(o => o.Car)
                .ThenInclude(i => i.CarType)
                .Include(i => i.Costumer)
                .AsNoTracking()
                .ToListAsync();
            return View(gMContext);
        }

        // GET: Orders/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var order = await _context.Order
                .Include(o => o.Car)
                .SingleOrDefaultAsync(m => m.OrderID == id);
            if (order == null)
            {
                return NotFound();
            }

            return View(order);
        }

        // GET: Orders/Create
        public IActionResult Create()
        {
            ViewData["CarID"] = new SelectList(_context.Car, "CarID", "CarID");
            return View();
        }
        public void CheckRentSpan(Order order)
        {

